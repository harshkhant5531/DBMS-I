SELECT REGION,AVG(SALES_AMOUNT) AS UP_AMOUNT FROM SALES_DATA WHERE YEAR='2023' GROUP BY REGION HAVING AVG(SALES_AMOUNT)>1500 ORDER BY AVG(SALES_AMOUNT) DESC

