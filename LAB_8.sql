---Display Count of Orders by Year and Region, Sorted by Year and Region
SELECT YEAR,REGION ,COUNT(PRODUCT) AS UP_DATA FROM SALES_DATA GROUP BY  YEAR,REGION  ORDER BY  YEAR,REGION 

---Display Regions with Maximum Sales Amount Exceeding 1000 in Any Year, Sorted by Region
SELECT REGION,YEAR,MAX(SALES_AMOUNT) AS UP_AMOUNT FROM SALES_DATA GROUP BY REGION,YEAR HAVING MAX(SALES_AMOUNT)>1000 ORDER BY REGION

---Display Years with Total Sales Amount Less Than 1000, Sorted by Year Descending
SELECT YEAR,SUM(SALES_AMOUNT) AS UP_AMOUNT FROM SALES_DATA GROUP BY YEAR HAVING SUM(SALES_AMOUNT)<1000 ORDER BY YEAR DESC

---Display Top 3 Regions by Total Sales Amount in Year 2024
SELECT TOP 3 REGION,SUM(SALES_AMOUNT) AS UP_AMOUNT FROM SALES_DATA WHERE YEAR='2024' GROUP BY REGION


