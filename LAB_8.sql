---Display Total Sales Amount by Region
SELECT  REGION,SUM(SALES_AMOUNT) AS UPDATED_DATA   FROM SALES_DATA GROUP BY REGION

---Display Average Sales Amount by Product
SELECT AVG(SALES_AMOUNT) AS UPDATED_AMOUNT FROM SALES_DATA GROUP BY PRODUCT

---Display Maximum Sales Amount by Year
SELECT YEAR, MAX(SALES_AMOUNT) AS UPDATED_AMOUNT FROM SALES_DATA GROUP BY YEAR

---Display Minimum Sales Amount by Region and Year
SELECT REGION,YEAR,MIN(SALES_AMOUNT) AS UPDATED_AMOUNT FROM SALES_DATA GROUP BY REGION,YEAR

---Count of Products Sold by Region
SELECT REGION,COUNT(PRODUCT) AS SOLD_PRODUCT FROM SALES_DATA GROUP BY REGION

---Display Sales Amount by Year and Product
SELECT COUNT(SALES_AMOUNT) AS UP_AMOUNT FROM SALES_DATA GROUP BY YEAR,PRODUCT

---Display Regions with Total Sales Greater Than 5000
SELECT REGION,COUNT(SALES_AMOUNT) AS UP_SALE FROM SALES_DATA GROUP BY REGION HAVING COUNT(SALES_AMOUNT)>5000

---Display Products with Average Sales Less Than 10000
SELECT PRODUCT,AVG(SALES_AMOUNT)AS UP_SALES FROM SALES_DATA GROUP BY PRODUCT HAVING AVG(SALES_AMOUNT)<10000

---Display Years with Maximum Sales Exceeding 500
SELECT YEAR,MAX(SALES_AMOUNT) AS UP_SALES FROM SALES_DATA GROUP BY YEAR HAVING MAX(SALES_AMOUNT)>500

---Display Regions with at Least 3 Distinct Products Sold.
SELECT REGION,COUNT(DISTINCT PRODUCT) AS UP_DATA FROM SALES_DATA GROUP BY REGION HAVING COUNT(DISTINCT PRODUCT)=3

---Display Years with Minimum Sales Less Than 1000
SELECT YEAR,MIN(SALES_AMOUNT) AS UPDATED_SALES FROM SALES_DATA HAVING MIN(SALES_AMOUNT)<1000

---Display Total Sales Amount by Region for Year 2023, Sorted by Total Amount
SELECT REGION,SUM(SALES_AMOUNT) AS TOTAL_AMOUNT FROM SALES_DATA WHERE YEAR = '2023' GROUP BY REGION ORDER BY SUM(SALES_AMOUNT)


